#include "libft.h"

int		ft_countchar(char const *s, char c)
{
	int		i;
	int		cc;

	cc = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			cc++;
		i++;
	}
	return (cc);
}
